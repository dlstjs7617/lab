package lv01;

public class Ex04 {

	public static void main(String[] args) {
		// 연산자
		
		// 산술 연산자
		// + 더하기 
		// - 빼기
		// * 곱하기
		// / 나누기 (정수/정수 => 몫, 정수/실수 => 소수점 자리 이하)
		// % 나머지
		
		System.out.println(10 + 3);
		System.out.println(10 - 3);
		System.out.println(10 * 3);
		System.out.println(10 / 3.0);
		System.out.println(10 % 3);
		
		// 문자열 확장
		// 문자열 + 문자(열) =>확장된 문자열
		// 문자열 + 정수 => 확장된 문자열
		System.out.println("가나다" + "라마바");
		System.out.println("가나다" + '라');
		System.out.println("문자열" + 1);
	}

}
