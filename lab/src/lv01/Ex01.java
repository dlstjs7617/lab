package lv01;

public class Ex01 {

	
		// 프로젝트가 실행 되면 가장 첫 번째로 실행 되는 메소드 : main
	public static void main(String[] args) {
		// scope 지역 : {} 중괄호의 시작과 끝을 의미

		// 주석 : 실행되지 않는 문장
		
		// 실행할 문장을 여기에 작성
		// 자동 완성 키 :sysout -> ctrl + space
		System.out.println("Hello World");
		// ; 세미콜론 실행문의 마침표 역할
		// print ->줄바꿈 x 
		// println ->줄바꿈 o
		// printf -> 정해진 형식대로 출력
		System.out.print(44);
		System.out.println(33);
		System.out.println(22);
		System.out.print(11);
		
		
		//run 실행 : ctrl + F11
	}

}
